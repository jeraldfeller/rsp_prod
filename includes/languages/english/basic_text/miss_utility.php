				<p><font size=\"+0\">March 7th, 2014</font></p>

                <p><font size=\"+0\">To our Valued Customers:</font></p>

				<p><font size=\"+0\">We are making minor updates to our install order placement process, in order to better manage Miss Utility requirments.</font></p>

                <p><font size=\"+0\">The changes are as follows:</font></p>

                <p><font size=\"+0\">1.  When you place an install order, you will be asked if you want us to call Miss Utility to mark the address for utilities.</font></p>

                <p><font size=\"+0\">2.  If you answer yes, your install date will be delayed three business days, to allow Miss Utility time to mark the property.  The normal installation window (two business day installation window for our core service area) will begin after the three business day delay.</font></p>

                <p><font size=\"+0\">NOTE: This means that your order may take up to a week to be completed to meet the Miss Utility requirements.</font></p>

				<p><font size=\"+0\">3.  If you answer no, you will be asked if there is a lamp in the yard, and if the lamp is gas.</font></p>

				<p><font size=\"+0\">4.  If there is a gas lamp, we will delay the install date by three business days, and call Miss Utility.  The normal installation window (two business day installation window for our core service area) will begin after the three business day delay.</font></p>

				<p><font size=\"+0\">NOTE: This means that your order may take up to a week to be completed to meet the Miss Utility requirements.</font></p>

				<p><font size=\"+0\">5.  If there is no gas lamp at the property, things will be as they currently are, and we will install the signpost as quickly as possible.</font></p>

				<p><font size=\"+0\">Feel free to contact us if you have any questions or concerns about these changes.</font></p>

